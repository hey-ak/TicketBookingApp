Future<http.Response> createAlbum(String title) {
  return http.post(
    Uri.parse('http://localhost:4000/api/users/login'),
    headers: <String, String>{
      'Content-Type': 'application/json; charset=UTF-8',
    },
    body: jsonEncode(<String, String>{
      'title': title,
    }),
  );
}

